#!/bin/bash

############################################################################################################
# Patch Instructions
# vaspFS.6.4-patch-1.0.1.sh
# created by Sheng Zhang, Institute of Physics, Chinese Academy of Sciences
# compile the script vaspFS.6.4
# step 1: copy the folder vasp.6.4 in the same folder as this script
# step 2: create and revise makefile in vasp.6.4 so that vasp_ncl can be compiled successfully (no need to compile)
# step 3: run this patch: bash vaspFS.6.4-patch-1.0.1.sh
# After these steps, wait a few miniutes and then if "Finishing installing vaspFS!" is shown on the shell
# then vaspFS is compiled successfully.
