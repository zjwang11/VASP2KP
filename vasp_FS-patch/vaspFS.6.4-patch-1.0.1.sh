##############################################################################
# begin

# judge if vasp2FS.6.4 already exists
if [ -d "vaspFS.6.4" ]; then
    echo "The file folder 'vaspFS.6.4' already exists, if you continue the installation, this folder will be deleted. Do you want to continue? [y/n]"
    read flag
    while [ 0 ]; do
        if [ "$flag" == "n" ] || [ "$flag" == "N" ]; then
            exit
        elif [ "$flag" == "y" ] || [ "$flag" == "Y" ]; then
            rm -rf vaspFS.6.4
            break
        else
            read flag
        fi
    done
fi

# prepare the files
mkdir vaspFS.6.4
cd vaspFS.6.4

mkdir bin
mkdir build
cp ../vasp.6.4/makefile.include .
cp -r ../vasp.6.4/src .

cd src
# create vp_fermi.f90
echo "$vp_fermi_f90" > vp_fermi.f90

# revise mkpoints_full.F
line_number=$(grep -n "LOGICAL :: LSKIP_IRREGULAR_POINTS" "mkpoints_full.F" | sed -n 's/^\([0-9]\+\):.*$/\1/p')
if [ -z "$line_number" ]; then
    echo "Error: cannot find the right line in mkpoints_full.F!!!"
    exit
fi

sed -i "${line_number}r /dev/stdin" mkpoints_full.F <<< "$mkpoints_full_rv1"

line_number=$(grep -n "INTEGER N1, N2, N3" "mkpoints_full.F" | sed -n 's/^\([0-9]\+\):.*$/\1/p')
if [ -z "$line_number" ]; then
    echo "Error: cannot find the right line in mkpoints_full.F!!!"
    exit
fi

sed -i "${line_number}r /dev/stdin" mkpoints_full.F <<< "      INTEGER NW,NZ"

line_number=$(grep -n "KPOINTS_F%NKPTS=NKTMP" "mkpoints_full.F" | sed -n 's/^\([0-9]\+\):.*$/\1/p')
if [ -z "$line_number" ]; then
    echo "Error: cannot find the right line in mkpoints_full.F!!!"
    exit
fi

sed -i "${line_number}r /dev/stdin" mkpoints_full.F <<< "$mkpoints_full_rv2"

line_number=$(grep -n "DEALLOCATE(K_TMP%VKPT, K_TMP%TRANS, K_TMP%LSHIFT, K_TMP%SPINFLIP, &" "mkpoints_full.F" | sed -n 's/^\([0-9]\+\):.*$/\1/p')
if [ -z "$line_number" ]; then
    echo "Error: cannot find the right line in mkpoints_full.F!!!"
    exit
fi

line_number=$(expr $line_number - 2)
sed -i "${line_number}r /dev/stdin" mkpoints_full.F <<< "$mkpoints_full_rv3"

cd ..

# revise makefile
if [ -e "../vasp.6.4/makefile" ]; then
  makefile="makefile"
elif [ -e "../vasp.6.4/Makefile" ]; then
  makefile="Makefile"
else
  echo "File 'Makefile' not exists in vasp.6.4!"
  exit
fi
cp ../vasp.6.4/$makefile ./

line_number=$(grep -n -E '^[[:space:]]*all[[:space:]]*:' $makefile | sed -n 's/^\([0-9]\+\):.*$/\1/p')
if [ -z "$line_number" ]; then
    echo "Error: cannot find the right line in ${makefile}!!!"
    exit
fi

sed -i "${line_number}s/.*/all: ncl  std/" $makefile

line_number=$(grep -n "cp src" "$makefile" | sed -n 's/^\([0-9]\+\):.*$/\1/p')
if [ -z "$line_number" ]; then
    echo "Error: cannot find the right line in $makefile!!!"
    exit
fi

sed -i "${line_number}r /dev/stdin" $makefile <<< "$(echo -e "\tmv vp_fermi bin/.")"
sed -i "${line_number}r /dev/stdin" $makefile <<< "$(echo -e "\tifort -o vp_fermi src/vp_fermi.f90")"
sed -i "${line_number}r /dev/stdin" $makefile <<< "$(echo -e "\tif [ ! -d bin ]; then mkdir -p bin ; fi")"


echo "Finish modifying vasp into vaspFS!"
echo "vaspFS source file is in the file folder vaspFS.6.4, do you want to compile it right now? [y/n]"

# ask if the user want to compile vaspFS right now
read flag_2
while [ 0 ]; do
    if [ "$flag_2" == "n" ] || [ "$flag_2" == "N" ]; then
        cd ..
        exit
    elif [ "$flag_2" == "y" ] || [ "$flag_2" == "Y" ]; then
        echo "Begin compiling vaspFS!"
        break
    else
        read flag_2
    fi
done

# begin compiling
make all

# rename vasp to vaspFS
cd build/ncl
mv vasp vasp_ncl_FS

# rename vasp to vaspFS
cd ../
cd std
mv vasp vasp_std_FS

cd ../../

mv bin/vasp_ncl bin/vasp_ncl_FS
mv bin/vasp_std bin/vasp_std_FS
cd bin

# path of the file folder of vaspFS
current_path=$(pwd)

cd ..

# print the path
echo " "
echo "Finishing installing vaspFS!"
echo "The path of vaspFS: $current_path/vasp_ncl_FS (or $current_path/vasp_std_FS)"
